package com.loopsdemo;

public class whileloop {
	public static void main(String[] args) {
		int a = 5;
		do {
					System.out.print(a+" ");
					a--;
		}
		while(a>=1);
			System.out.println();
	
	for(int aa = 1; aa<=5;aa++)
	{
		System.out.print(aa+" ");
		
	}
	System.out.println();
	int b=5;
	while(b>=1)
	{
		System.out.print(b+" ");
		b--;
	}

}
}